static uint8_t lcd_buf[LCD_Buf_Size];
/*****************************************************************************
 * @name       :void LCD_Clear(uint16_t Color)
 * @date       :2018-08-09 
 * @function   :Full screen filled LCD screen
 * @parameters :color:Filled color
 * @retvalue   :None
******************************************************************************/	
void LCD_Clear(uint16_t Color)
{
    uint16_t i, j;
    uint8_t data[2] = {0};  //color是16bit的，每个像素点需要两个字节的显存
    /* 将16bit的color值分开为两个单独的字节 */
    data[0] = Color >> 8;
    data[1] = Color;
    /* 显存的值需要逐字节写入 */
    for(j = 0; j < LCD_Buf_Size / 2; j++)
    {
        lcd_buf[j * 2] =  data[0];
        lcd_buf[j * 2 + 1] =  data[1];
    }
	
	LCD_SetWindows(0,0,lcddev.width-1,lcddev.height-1);   
	LCD_CS_SET(0);
	LCD_RS_SET(1);
    for(i = 0; i < (LCD_TOTAL_BUF_SIZE / LCD_Buf_Size); i++)
    {
        spi1_write_byt(lcd_buf, (uint16_t)LCD_Buf_Size);
    }
	LCD_CS_SET(1);
} 

/*****************************************************************************
 * @name       :void LCD_Clear(uint16_t Color)
 * @date       :2018-08-09 
 * @function   :Initialization LCD screen GPIO
 * @parameters :None
 * @retvalue   :None
******************************************************************************/	
void LCD_GPIOInit(void)
{
	GPIO_InitTypeDef GPIO_InitStruct = {0};
	LCD_CS_CLK_ENABLE();
	LCD_RS_CLK_ENABLE();
	LCD_RST_CLK_ENABLE();
	
	GPIO_InitStruct.Pin = LCD_CS_PIN;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;            /* 推挽输出 */
	GPIO_InitStruct.Pull = GPIO_NOPULL ;                   /* 使能下拉 */
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;          /* GPIO速度 */
	HAL_GPIO_Init(LCD_CS_PORT, &GPIO_InitStruct);                /* GPIO初始化 */
	
	GPIO_InitStruct.Pin = LCD_RS_PIN;
	HAL_GPIO_Init(LCD_RS_PORT, &GPIO_InitStruct);                /* GPIO初始化 */
	
	GPIO_InitStruct.Pin = LCD_RST_PIN;
	HAL_GPIO_Init(LCD_RST_PORT, &GPIO_InitStruct);                /* GPIO初始化 */
	
}

/*****************************************************************************
 * @name       :void LCD_RESET(void)
 * @date       :2018-08-09 
 * @function   :Reset LCD screen
 * @parameters :None
 * @retvalue   :None
******************************************************************************/	
void LCD_RESET(void)
{
	LCD_RST_SET(0);
	HAL_Delay(100);	
	LCD_RST_SET(1);
	HAL_Delay(50);
}

